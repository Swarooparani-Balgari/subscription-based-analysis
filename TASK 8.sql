----- CREATE TABLE------

CREATE TABLE SUBSCRIPTIONS(
USER_ID INT PRIMARY KEY,
JOIN_DATE DATE,
CITY VARCHAR(20),
SUBSCRIPTION_PLAN VARCHAR(20),
MONTHLY_FEE INT,
MONTHS_ACTIVE INT,
DEVICE VARCHAR(20),
PAYMENT_TYPE VARCHAR(20)
);

