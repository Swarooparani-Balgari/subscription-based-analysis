----- TOTAL REVENUE ------

SELECT
  SUM(MONTHLY_FEE*MONTHS_ACTIVE) AS TOTAL_REVENUE
FROM SUBSCRIPTIONS;

----- REVENUE BY PLAN ------

SELECT
  SUBSCRIPTION_PLAN,
  SUM(MONTHLY_FEE*MONTHS_ACTIVE) AS TOTAL_REVENUE
FROM SUBSCRIPTIONS
GROUP BY SUBSCRIPTION_PLAN
ORDER BY TOTAL_REVENUE DESC;

----- AVERAGE RETENTION -----

SELECT
  SUBSCRIPTION_PLAN,
  SUM(MONTHS_ACTIVE) AS AVG_MONTHS
FROM SUBSCRIPTIONS
GROUP BY SUBSCRIPTION_PLAN;

----- USERS BY DEVICE ------

SELECT 
  DEVICE,
  COUNT(*) AS USERS
  FROM SUBSCRIPTIONS
GROUP BY DEVICE;

----- REVENUE BY DEVICE -----

SELECT
  DEVICE,
  SUM(MONTHLY_FEE*MONTHS_ACTIVE) AS TOTAL_REVENUE
FROM SUBSCRIPTIONS
GROUP BY DEVICE
ORDER BY TOTAL_REVENUE DESC;

----- REVENUE BY CITY -----

SELECT
  CITY,
  SUM(MONTHLY_FEE*MONTHS_ACTIVE) AS TOTAL_REVENUE
FROM SUBSCRIPTIONS
GROUP BY CITY
ORDER BY TOTAL_REVENUE DESC;

----- REVENUE BY PAYMENT_TYPE -----

SELECT
  PAYMENT_TYPE,
  SUM(MONTHLY_FEE*MONTHS_ACTIVE) AS TOTAL_REVENUE
FROM SUBSCRIPTIONS
GROUP BY PAYMENT_TYPE
ORDER BY TOTAL_REVENUE DESC;